<h3>Finding YouTube Embed Code</h3>
<p>To find the YouTube embed code navigate to the video you want to share, then hit "Share", then "Embed". You should see some HTML code in the box - <strong>copy the contents of that box</strong> then <strong>paste it into the Screenr embed code box</strong> under your new blog post.</p>
<h3>To display the video in your content</h3>
<p>Write the shortcode "[video]" into your content where you want the video to appear.</p>
